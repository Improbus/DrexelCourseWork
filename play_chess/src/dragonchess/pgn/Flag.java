package dragonchess.pgn;

/**
 * @author Damola Mabogunje
 *
 */
public enum Flag 
{
	CAPTURE("x"),
	PROMOTE("="),
	CHECK("+"),
	CHECKMATE("#"),
	KCASTLE("O-O"),
	QCASTLE("O-O-O"),
	AMBIGUOUS("AMB");
	
	private String flag;
	
	private Flag(String flag)
	{
		this.flag = flag;
	}
	
	@Override
	public String toString() 
	{
		return String.valueOf(this.flag);
	}
	
}
