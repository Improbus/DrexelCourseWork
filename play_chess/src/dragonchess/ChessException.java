package dragonchess;

/**
 *
 * @author Group 4
 */
public class ChessException extends Exception {

	private static final long serialVersionUID = 123456789L;

	/**
	 * Create a new ChessException with null as its detail message.
	 */
	public ChessException() {
		super();

		return;
	}

	/**
	 * Create a new ChessException with the specified detail message.
	 *
	 * @param message
	 */
	public ChessException(String message) {
		super(message);

		return;
	}

}
